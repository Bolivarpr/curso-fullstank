/*
operadores de comparação

> - maior que
>= - maior ou igual a
< - menor que
<= - menor ou igual a
== - igualdade (valor)
=== - igualdade estrita (valor e tipo)
!= - diferente (valor)
!== -  diferente estrito (valor e tipo)
*/

let num1 = 6;
let num2 = 8;
const resultado = num1 !== num2;
console.log(resultado);